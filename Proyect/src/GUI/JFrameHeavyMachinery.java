    private void jButtonSearchSerieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchSerieActionPerformed
        try {
            int serie = Integer.parseInt(jTextFieldSearchSerie.getText());

            heavyMachineryTemp = fileHeavyMachinery.searchBySeries(serie);

            if (heavyMachineryTemp != null) {
                jTextFieldUpdateName.setEditable(true);
                jTextFieldUpdateYear.setEditable(true);
                jComboBoxAmerican.setEnabled(true);
                jButtonUpdateVehicle.setEnabled(true);
                jTextFieldSearchSerie.setEditable(false);
                jButtonSearchSerie.setEnabled(false);

                jTextFieldUpdateName.setText(heavyMachineryTemp.getName());
                jTextFieldUpdateYear.setText(String.valueOf(heavyMachineryTemp.getAge()));
                if (heavyMachineryTemp.isAmericano() == true) {
                    jLabelUpdateAmerican.setText("Yes");
                } else {
                    jLabelUpdateAmerican.setText("No");
                }

                jTextFieldUpdateMileage.setText(String.valueOf(heavyMachineryTemp.getMileage()));
                jTextFieldUpdateWeight.setText(String.valueOf(heavyMachineryTemp.getWeight()));
                jTextFieldSearchSerie.setText(String.valueOf(heavyMachineryTemp.getSerie()));

            }
        } catch (IOException ex) {
            Logger.getLogger(JFrameHeavyMachinery.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonSearchSerieActionPerformed

